# Operatory logiczne posługują się AND oraz OR (czyli i oraz lub)
a = 10

print(a > 2 and a > 10)
# Operator AND sprawdza czy w dwóch przypadkach jest TRUE, jesli coś się nie zgadza, czyli w jednym mamy TRUE, a w drugi FAlSE. To i tak otrzymamy wartość FALSE

print(a < 2 or a <= 10)
# Operator OR, sprzawdza czy ktoryś z tych warunków jest prawdziwy i jeśli, któryś z tych warunkow jest prawdziwy to zwraca wartość TRUE